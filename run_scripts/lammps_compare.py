"""
run_scripts/lammps_compare.py
=============================
Simulon vs LAMMPS 精度对比脚本

测试 1 — LJ (Ar, 100 atoms, NVE)
测试 2 — EAM/W (100 atoms, NVE, WRe_YC2.eam.fs)

用法
----
python run_scripts/lammps_compare.py [--lammps /path/to/lmp]

脚本做三件事：
1. 用 Simulon 计算 t=0 静态量（能量、力）和 NVE 200 步动力学
2. 生成等价的 LAMMPS 输入文件（*.lmp）到 run_output/lammps_compare/
3. 若指定 --lammps，自动运行 LAMMPS 并读取输出做数值对比；
   否则打印手动运行说明

关键对比量
----------
  E_pot/atom   — 势能（静态），eV/atom
  max|F|       — 最大力幅值，eV/Ang（静态）
  mean|F|      — 平均力幅值，eV/Ang（静态）
  ΔE_total     — NVE 200 步总能漂移，eV（应 < 0.5%）
"""
from __future__ import annotations
import os, sys, argparse, subprocess, time
sys.path.insert(0, os.path.join(os.path.dirname(__file__), '..'))

import numpy as np
import torch

PROJECT = os.path.dirname(os.path.dirname(os.path.abspath(__file__)))
OUTDIR  = os.path.join(PROJECT, 'run_output', 'lammps_compare')
os.makedirs(OUTDIR, exist_ok=True)

# ─── CLI ─────────────────────────────────────────────────────────────────────
parser = argparse.ArgumentParser()
parser.add_argument('--lammps', type=str, default='',
                    help='Path to LAMMPS executable (lmp / lmp_serial / lmp_mpi)')
args = parser.parse_args()


# ─── helpers ─────────────────────────────────────────────────────────────────
DEVICE = 'cuda' if torch.cuda.is_available() else 'cpu'

def _sep(title=''):
    w = 62
    if title:
        print(f"\n{'─'*4} {title} {'─'*(w-6-len(title))}")
    else:
        print('─' * w)

def _run_simulon_static(mol, ff_cls, ff_kwargs=None):
    """Compute E_pot and forces at t=0 (no dynamics)."""
    from core.md_model import SumBackboneInterface
    ff = ff_cls(mol, **(ff_kwargs or {}))
    sb = SumBackboneInterface([ff], mol)
    with torch.no_grad():
        out = sb()
    E = float(out['energy'].cpu())
    F = out['forces'].detach().cpu().numpy()
    return E, F

def _run_simulon_nve(mol, ff_cls, ff_kwargs, n_steps, dt):
    """Short NVE run. Returns (E_pot_0, E_tot_0, E_pot_N, E_tot_N)."""
    from core.md_model import SumBackboneInterface, BaseModel
    from core.integrator.integrator import VerletIntegrator
    ff = ff_cls(mol, **(ff_kwargs or {}))
    sb = SumBackboneInterface([ff], mol)
    integ = VerletIntegrator(mol, dt=dt, ensemble='NVE')
    model = BaseModel(sb, integ, mol)
    energies = []
    with torch.no_grad():
        for step in range(n_steps):
            out = model()
            E_pot = float(out['energy'].cpu())
            E_kin = float(out['kinetic_energy'].cpu())
            energies.append((E_pot, E_kin))
    return energies


def _write_lammps_data(path, xyz_path, box_length, atom_mass_amu,
                       element_symbol='Ar', element_id=1):
    """Write a LAMMPS data file from xyz."""
    coords = []
    with open(xyz_path) as f:
        n = int(f.readline())
        f.readline()
        for _ in range(n):
            p = f.readline().split()
            coords.append([float(p[1]), float(p[2]), float(p[3])])
    L = box_length
    with open(path, 'w') as f:
        f.write(f"# Generated by lammps_compare.py\n\n")
        f.write(f"{n} atoms\n1 atom types\n\n")
        f.write(f"0.0 {L:.6f} xlo xhi\n")
        f.write(f"0.0 {L:.6f} ylo yhi\n")
        f.write(f"0.0 {L:.6f} zlo zhi\n\n")
        f.write("Masses\n\n")
        f.write(f"1 {atom_mass_amu:.4f}  # {element_symbol}\n\n")
        f.write("Atoms  # atomic\n\n")
        for i, (x, y, z) in enumerate(coords, 1):
            f.write(f"{i} {element_id} {x:.8f} {y:.8f} {z:.8f}\n")
    return n, coords


