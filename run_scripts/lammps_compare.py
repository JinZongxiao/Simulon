def _parse_lammps_thermo(log_path):
    """Parse LAMMPS log, return dict of lists for each thermo column."""
    cols, data = None, {}
    in_thermo = False
    with open(log_path) as f:
        for line in f:
            line = line.strip()
            if line.startswith('Step') and 'TotEng' in line or \
               line.startswith('Step') and 'PotEng' in line or \
               line.startswith('Step') and 'Temp' in line:
                cols = line.split()
                for c in cols:
                    data[c] = []
                in_thermo = True
                continue
            if in_thermo:
                parts = line.split()
                if len(parts) == len(cols):
                    try:
                        for c, v in zip(cols, parts):
                            data[c].append(float(v))
                    except ValueError:
                        in_thermo = False
                else:
                    in_thermo = False
    return data

def _parse_lammps_dump_forces(dump_path):
    """Parse LAMMPS force dump, return Nx3 array."""
    forces = {}
    with open(dump_path) as f:
        lines = f.readlines()
    i = 0
    while i < len(lines):
        if lines[i].strip() == 'ITEM: ATOMS id fx fy fz':
            i += 1
            while i < len(lines) and not lines[i].startswith('ITEM:'):
                p = lines[i].split()
                if len(p) == 4:
                    forces[int(p[0])] = [float(p[1]), float(p[2]), float(p[3])]
                i += 1
        else:
            i += 1
    if not forces:
        return None
    n = max(forces.keys())
    arr = np.zeros((n, 3))
    for idx, fvec in forces.items():
        arr[idx-1] = fvec
    return arr

if args.lammps:
    for tag, inp, log, F_sim, E_sim, N_atoms in [
        ('LJ/Ar',  lj_in,  os.path.join(OUTDIR,'lj_ar.log'),
         F_lj, E_lj, N_lj),
        ('EAM/W',  eam_in, os.path.join(OUTDIR,'eam_w.log'),
         F_eam, E_eam, N_eam),
    ]:
        _sep(f'LAMMPS comparison: {tag}')
        try:
            ret = subprocess.run(
                [args.lammps, '-in', inp, '-log', log],
                cwd=OUTDIR, capture_output=True, text=True, timeout=300
            )
            if ret.returncode != 0:
                print(f"LAMMPS error:\n{ret.stderr[-2000:]}")
                continue
        except Exception as e:
            print(f"Failed to run LAMMPS: {e}")
            continue

        thermo = _parse_lammps_thermo(log)
        if 'PotEng' in thermo and thermo['PotEng']:
            E_lmp = thermo['PotEng'][0]  # eV, step 0
            print(f"  LAMMPS E_pot/atom = {E_lmp/N_atoms:.6f} eV/atom")
            print(f"  Simulon E_pot/atom= {E_sim/N_atoms:.6f} eV/atom")
            print(f"  diff              = {abs(E_lmp - E_sim)/N_atoms*1000:.4f} meV/atom")
        else:
            print(f"  (could not parse thermo from {log})")

        # Force comparison
        dmp_path = os.path.join(OUTDIR, f"{'lj' if 'LJ' in tag else 'eam'}_forces.dump")
        F_lmp = _parse_lammps_dump_forces(dmp_path)
        if F_lmp is not None:
            dF = np.linalg.norm(F_sim - F_lmp, axis=1)
            print(f"  Force MAE        = {dF.mean():.4e} eV/Ang")
            print(f"  Force max err    = {dF.max():.4e} eV/Ang")
        else:
            print(f"  (force dump not found at {dmp_path})")
else:
    _sep('How to run LAMMPS manually')
    print("  lmp -in run_output/lammps_compare/lj_ar.in  -log run_output/lammps_compare/lj_ar.log")
    print("  lmp -in run_output/lammps_compare/eam_w.in  -log run_output/lammps_compare/eam_w.log")
    print()
    print("  Then compare LAMMPS log vs Simulon output above:")
    print(f"  LJ  Simulon E_pot/atom = {E_lj/N_lj:.6f} eV/atom")
    try:
        print(f"  EAM Simulon E_pot/atom = {E_eam/N_eam:.6f} eV/atom")
    except NameError:
        pass
    print()
    print("  Key LAMMPS thermo column: 'PotEng' (= E_pot in eV)")
    print("  After NVE run: check 'TotEng' drift < 0.1% for energy conservation.")
    print()
    print("  Re-run with --lammps flag for automatic comparison:")
    print("  python run_scripts/lammps_compare.py --lammps /path/to/lmp")

_sep()
print("Done. Output directory:", OUTDIR)
