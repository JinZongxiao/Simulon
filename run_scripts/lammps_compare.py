# TEST 1 — LJ / Ar
# ═══════════════════════════════════════════════════════════════════════════════
_sep('TEST 1: LJ Ar  (100 atoms, NVE)')

LJ_XYZ     = os.path.join(PROJECT, 'run_data', 'Ar', 'Ar.xyz')
LJ_BOX     = 16.901   # Å
LJ_CUTOFF  = 8.5      # Å  (= rc, no tail correction, matches LAMMPS lj/cut)
LJ_EPS     = 0.0104   # eV
LJ_SIG     = 3.405    # Å
LJ_MASS    = 39.948   # g/mol
LJ_DT      = 0.001    # ps = 1 fs
LJ_NSTEPS  = 200

from io_utils.reader import AtomFileReader
from core.force.lennard_jones_force import LennardJonesForce

mol_lj = AtomFileReader(
    LJ_XYZ, box_length=LJ_BOX, cutoff=LJ_CUTOFF,
    device=DEVICE, parameter={'[0 0]': {'epsilon': LJ_EPS, 'sigma': LJ_SIG}},
    skin_thickness=2.0,
)

# ── Simulon static ────────────────────────────────────────────────────────────
E_lj, F_lj = _run_simulon_static(mol_lj, LennardJonesForce)
N_lj = mol_lj.atom_count
Fmag_lj = np.linalg.norm(F_lj, axis=1)
print(f"[Simulon] N={N_lj}")
print(f"  E_pot        = {E_lj:.6f} eV  ({E_lj/N_lj:.6f} eV/atom)")
print(f"  max|F|       = {Fmag_lj.max():.6f} eV/Ang")
print(f"  mean|F|      = {Fmag_lj.mean():.6f} eV/Ang")

# ── Simulon NVE ───────────────────────────────────────────────────────────────
# Re-read to get fresh velocities (v=0 for NVE comparison)
mol_lj2 = AtomFileReader(
    LJ_XYZ, box_length=LJ_BOX, cutoff=LJ_CUTOFF,
    device=DEVICE, parameter={'[0 0]': {'epsilon': LJ_EPS, 'sigma': LJ_SIG}},
    skin_thickness=2.0,
)
# Set velocities to zero for clean NVE energy conservation test
mol_lj2.atom_velocities.zero_()

energies_lj = _run_simulon_nve(mol_lj2, LennardJonesForce, {}, LJ_NSTEPS, LJ_DT)
E_tot_lj_0 = energies_lj[0][0] + energies_lj[0][1]
E_tot_lj_N = energies_lj[-1][0] + energies_lj[-1][1]
drift_lj = abs(E_tot_lj_N - E_tot_lj_0)
drift_pct_lj = 100 * drift_lj / max(abs(E_tot_lj_0), 1e-12)
print(f"\n[Simulon NVE {LJ_NSTEPS} steps, dt={LJ_DT} ps, v0=0]")
print(f"  E_tot step 0 = {E_tot_lj_0:.6f} eV")
print(f"  E_tot step N = {E_tot_lj_N:.6f} eV")
print(f"  |drift|      = {drift_lj:.4e} eV  ({drift_pct_lj:.3f}%)")

# ── Generate LAMMPS input ─────────────────────────────────────────────────────
lj_data = os.path.join(OUTDIR, 'lj_ar.data')
lj_in   = os.path.join(OUTDIR, 'lj_ar.in')
_write_lammps_data(lj_data, LJ_XYZ, LJ_BOX, LJ_MASS, 'Ar', 1)

with open(lj_in, 'w') as f:
    f.write(f"""\
# LAMMPS LJ/Ar comparison  (generated by lammps_compare.py)
# Match: pair_modify tail no  (Simulon has no tail correction)
units        metal
atom_style   atomic
boundary     p p p
read_data    {lj_data}

pair_style   lj/cut {LJ_CUTOFF:.4f}
pair_coeff   1 1 {LJ_EPS:.6f} {LJ_SIG:.6f}
pair_modify  tail no    # IMPORTANT: Simulon has no tail correction

timestep     {LJ_DT:.4f}
velocity     all set 0.0 0.0 0.0     # v0=0, same as Simulon NVE test

thermo_style custom step etotal pe ke temp press
thermo       1
fix          1 all nve
run          {LJ_NSTEPS}

# Force dump at step 0 (before run)
dump         frc all custom 1 {os.path.join(OUTDIR, 'lj_forces.dump').replace(chr(92),'/')} id fx fy fz
dump_modify  frc first yes
run          0
undump       frc
""")

print(f"\n[LAMMPS input] written to:\n  data: {lj_data}\n  in:   {lj_in}")


